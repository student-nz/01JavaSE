package com.yj.nz.d15_integer.demo1;

public class Test1 {
    public static void main(String[] args) {
        // 基本数据类型 不是类 没有方法和属性可以让我们调用
        int a=10;
        // 将基本数据类型包装成类
        // Integer类 类中有一些现成的功能功我们使用
        Integer b =10;
        System.out.println(a);
        System.out.println(b);


        // 八种基本数据类型 对应着自己的包装类
        byte c=10;
        Byte c2 =10;


    }
}
